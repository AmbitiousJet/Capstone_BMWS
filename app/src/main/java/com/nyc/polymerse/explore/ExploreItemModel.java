public class ExploreItemModel {

    private String song;
    private String song_img;
    private String song_url;
    private String show_movie;
    private String show_movie_img;
    private String show_movie_imdb;
    private String place;
    private String place_img;
    private String place_url;

    public String getSong() {
        return song;
    }

    public void setSong(String song) {
        this.song = song;
    }

    public String getSong_img() {
        return song_img;
    }

    public void setSong_img(String song_img) {
        this.song_img = song_img;
    }

    public String getSong_url() {
        return song_url;
    }

    public void setSong_url(String song_link) {
        this.song_url = song_link;
    }

    public String getShow_movie() {
        return show_movie;
    }

    public void setShow_movie(String show_movie) {
        this.show_movie = show_movie;
    }

    public String getShow_movie_img() {
        return show_movie_img;
    }

    public void setShow_movie_img(String show_movie_img) {
        this.show_movie_img = show_movie_img;
    }

    public String getShow_movie_imdb() {
        return show_movie_imdb;
    }

    public void setShow_movie_imdb(String show_movie_imdb) {
        this.show_movie_imdb = show_movie_imdb;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getPlace_img() {
        return place_img;
    }

    public void setPlace_img(String place_img) {
        this.place_img = place_img;
    }

    public String getPlace_url() {
        return place_url;
    }

    public void setPlace_url(String place_url) {
        this.place_url = place_url;
    }
}
